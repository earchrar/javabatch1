public class L5assignmentoperator{

    public static void main(String[] args){

        //  Assignment Operators ( = , += , -= , *= , %= )

            // (+=)
            int a = 10;
            a += 5;
            System.out.printf("Number is = %s\n",a);

            // (-=)
            int b = 10;
            b += 5;
            System.out.printf("Number is = %s\n",b);

            // (*=)
            int c = 10;
            c *= 5;
            System.out.printf("Number is = %s\n",c);

            // (%=)
            int d = 10;
            d *= 5;
            System.out.printf("Number is = %s",d);

    }

}